    BILLING_ANOMALIES.CUSTOMER_NAME AS CUSTOMER_NAME,
    BILLING_ANOMALIES.TXN_DATE AS TXN_DATE,
    BILLING_ANOMALIES.SERVICE_TYPE AS SERVICE_TYPE,
    BILLING_ANOMALIES.TEMPERATURE AS TEMPERATURE,
    BILLING_ANOMALIES.CONTAINER_SIZE AS CONTAINER_SIZE,
