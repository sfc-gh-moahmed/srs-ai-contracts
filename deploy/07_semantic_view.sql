    BILLING_ANOMALIES.ANOMALY_TYPE AS ANOMALY_TYPE,
    BILLING_ANOMALIES.CONTRACT_START AS CONTRACT_START,
    BILLING_ANOMALIES.CONTRACT_END AS CONTRACT_END
